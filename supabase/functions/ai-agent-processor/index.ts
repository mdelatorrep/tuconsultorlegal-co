// Helper function to get system configuration
async function getSystemConfig(supabaseClient: any, configKey: string, defaultValue?: string): Promise<string> {
  try {
    console.log(`üîß Fetching config: ${configKey}`);
    const { data, error } = await supabaseClient
      .from('system_config')
      .select('config_value')
      .eq('config_key', configKey)
      .maybeSingle();

    if (error) {
      console.error(`‚ùå Error fetching config ${configKey}:`, error);
      return defaultValue || '';
    }

    const value = data?.config_value || defaultValue || '';
    console.log(`‚úÖ Config ${configKey}: ${value.substring(0, 50)}${value.length > 50 ? '...' : ''}`);
    return value;
  } catch (error) {
    console.error(`‚ùå Exception fetching config ${configKey}:`, error);
    return defaultValue || '';
  }
}

// Helper function to extract placeholders from template
function extractPlaceholders(docTemplate: string) {
  console.log('üîç Extracting placeholders from template...');
  const placeholderRegex = /\{\{([^}]+)\}\}/g;
  const placeholders = [];
  const found = new Set(); // Para evitar duplicados
  let match;
  
  while ((match = placeholderRegex.exec(docTemplate)) !== null) {
    const fieldName = match[1].trim();
    if (!found.has(fieldName)) {
      found.add(fieldName);
      placeholders.push({
        field: fieldName,
        label: fieldName.replace(/_/g, ' ').replace(/\b\w/g, l => l.toUpperCase()),
        type: 'text',
        required: true,
        description: `Ingrese ${fieldName.toLowerCase().replace(/_/g, ' ')}`
      });
    }
  }
  
  console.log(`‚úÖ Found ${placeholders.length} unique placeholders`);
  return placeholders;
}

// Helper function to call OpenAI API
async function callOpenAI(apiKey: string, model: string, messages: any[], temperature = 0.7, maxTokens = 1000) {
  console.log(`ü§ñ Calling OpenAI API with model: ${model}`);
  
  const response = await fetch('https://api.openai.com/v1/chat/completions', {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${apiKey}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      model,
      messages,
      temperature,
      max_tokens: maxTokens
    })
  });

  if (!response.ok) {
    const errorText = await response.text();
    console.error(`‚ùå OpenAI API error (${response.status}):`, errorText);
    throw new Error(`OpenAI API error: ${response.status} - ${errorText}`);
  }

  const data = await response.json();
  console.log(`‚úÖ OpenAI API response received`);
  return data;
}

